function calculateFire() {
    const birthdate = document.getElementById('birthdate').value;
    if (!birthdate) {
        alert('Будь ласка, введіть дату народження');
        return;
    }
    
    const date = new Date(birthdate);
    const day = date.getDate();
    
    const positive = positiveAspects[day] || "Опис в розробці...";
    const shadow = shadowAspects[day] || "Тіньовий аспект в розробці...";
    
    const result = `
        <div class="fire-title">Ваш Внутрішній Вогонь (День ${day})</div>
        <p>${positive}</p>
        <div class="fire-title" style="margin-top: 20px;">Тіньовий Аспект</div>
        <p>${shadow}</p>
    `;
    
    document.getElementById('result').innerHTML = result;
}

